import UIKit

extension UIColor {

    class func TurntPink() -> UIColor {
        return UIColor(red:255.0/255.0, green:0.0/255.0, blue:81.0/255.0, alpha:1.0)
    }
    
    class func TurntDarkPink() -> UIColor {
        return UIColor(red:228.0/255.0, green:0.0/255.0, blue:71.0/255.0, alpha:1.0)
    }
    
    class func TurntVeryDarkPink() -> UIColor {
        return UIColor(red:194.0/255.0, green:0.0/255.0, blue:62.0/255.0, alpha:1.0)
    }
    
    class func TurntLightGray() -> UIColor {
        return UIColor(red:242/255.0, green:242.0/255.0, blue:242.0/255.0, alpha:1.0)
    }
    
    class func TurntDarkGray() -> UIColor {
        return UIColor(red:228.0/255.0, green:228.0/255.0, blue:228.0/255.0, alpha:1.0)
    }
    
    class func TurntGreenLumo() -> UIColor {
        return UIColor(red:0.0/255.0, green:224.0/255.0, blue:72.0/255.0, alpha:1.0)
    }
    
    class func TurntWhite() -> UIColor {
        return UIColor(red:255.0/255.0, green:255.0/255.0, blue:255.0/255.0, alpha:1.0)
    }
    
    class func TurntChatTextColor() -> UIColor {
        return UIColor(red:102.0/255.0, green:102.0/255.0, blue:102.0/255.0, alpha:1.0)
    }
}
